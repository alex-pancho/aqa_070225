"""
Взагалі у театральному гуртку - 24 хлопчики, а дівчаток - вдвічі менше.
1 хлопчик захворів та 2 дівчинки не прийшли сьогодні.
Скількі сьогодні дітей у театральному гуртку?
"""
boys = 24
girls = boys // 2
# 1 мальчик заболел, и 2 девочки не пришли.
today_boys = boys - 1
today_girls = girls - 2
# Сколько сегодня детей в театральном кружке?
today_total = today_boys + today_girls

print(f"В театральном кружке всего: {boys} мальчика и {girls} девочек (потому что 24 делим на 2, и получается {girls} девочек).")
print(f"Сегодня в кружке {today_boys} мальчиков, так как один мальчик заболел.")
print(f"Сегодня в кружке {today_girls} девочек, потому что две девочки не пришли.")
print(f"Всего детей в кружке сегодня: {today_total} человек, это мальчики и девочки вместе.")
